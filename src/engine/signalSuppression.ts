// 부수적인 정상 조각이 같은 메시지의 실제 요청을 덮어 끄지 못하게 보호할 신호(행동 신호).
const REQUEST_OVERRIDABLE_SIGNALS: ReadonlySet<SignalId> = new Set<SignalId>([
  'financialLoss',
  'requestedAction',
  'malwareApp',
  'personalInfo',
]);

export function shouldSuppressSignal(signalId: SignalId, text: string): boolean {
  // 1) 메타 서술(교육/뉴스/예방/인용) 문맥이고 직접 명령형(광의)이 없으면 bypass보다 우선해 항상 억제.
  //    (직접 명령형이 함께 있으면 회피 시도이므로 억제하지 않고 아래 단계로 보낸다.)
  if (!LIVE_IMPERATIVE_BROAD.test(text) && META_DESCRIPTIVE_CONTEXT.test(text)) return true;

  const credentialSelfHelp = CREDENTIAL_SELF_HELP_CONTEXT.test(text);
  const liveCredentialImperative = LIVE_CREDENTIAL_IMPERATIVE.test(text);
  const liveCredentialRequest = LIVE_CREDENTIAL_REQUEST.test(text);
  const credentialSelfHelpCompletion = CREDENTIAL_SELF_HELP_COMPLETION.test(text);
  const credentialSelfHelpSafetyInformation = CREDENTIAL_SELF_HELP_SAFETY_INFORMATION.test(text);
  const selfHelpInformationRequest = CREDENTIAL_SELF_HELP_INFORMATION_REQUEST.test(text);
  const mixedSelfHelpWithDisclosureRequest = MIXED_SELF_HELP_WITH_DISCLOSURE_REQUEST.test(text);
  if (
    CREDENTIAL_SELF_HELP_SIGNALS.has(signalId) &&
    (credentialSelfHelpCompletion || credentialSelfHelpSafetyInformation)
  ) {
    return true;
  }
  if (
    CREDENTIAL_SELF_HELP_SIGNALS.has(signalId) &&
    credentialSelfHelp &&
    ((!liveCredentialImperative && !liveCredentialRequest) ||
      (selfHelpInformationRequest && !mixedSelfHelpWithDisclosureRequest))
  ) {
    return true;
  }

  // 2) 기존 bypass: 실제 공격 문맥을 복원한다.
  if ((SUPPRESSION_BYPASS_PATTERNS[signalId] ?? []).some((pattern) => pattern.test(text))) return false;

  // 3) 강한 정상 문맥 억제. 가족 송금 의도처럼 공격 명령형으로 보기 어려운 문장은
  //    구어체 동사 조각이 있어도 위험 요청 override를 태우지 않는다.
  if ((STRONG_BENIGN_CONTEXT_PATTERNS[signalId] ?? []).some((pattern) => pattern.test(text))) return true;

  // 4) 정상 문맥 억제. 단, 같은 메시지에 살아있는 금전/인증/설치 위험 요청(협의)이 있으면
  //    부수적 정상 조각(예: "병원비 영수증은 내가 챙길게")이 실제 요청을 덮어 끄지 못하게 한다.
  const benign = (BENIGN_CONTEXT_PATTERNS[signalId] ?? []).some((pattern) => pattern.test(text));
  if (
    benign &&
    REQUEST_OVERRIDABLE_SIGNALS.has(signalId) &&
    (LIVE_RISK_REQUEST.test(text) || LIVE_CREDENTIAL_REQUEST.test(text))
  ) {
    return false;
  }
  return benign;
}
